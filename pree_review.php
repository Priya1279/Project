                  <div class="panel panel-default">
               <div class="panel-heading" id="headingOne" role="tab">
           <h4 class="panel-title big text-info"><a href="#" class="text-dark">Introduction to peer review:</a></h4>
</div>
<hr>


		<div class="panel panel-default">
		<div class="panel-heading" id="headingTwo" role="tab">
        <p align="justify">Peer review exists to ensure that journals publish good science. This benefits the entire scientific community.
<br>
Sometimes scientists find the peer review process intimidating because it can lead to the rejection of their manuscript. Keep in mind that revisions and improvement are part of the publication process and actually help raise the quality of your manuscript.</p>
       <div class="panel-heading" id="headingOne" role="tab">
           <h6 class="panel-title big text-info"><a href="#">Peer review is a positive process:</a></h6>
</div><hr>
<p align="justify">Peer review is an integral part of scientific publishing that confirms the validity of the science reported. Peer reviewers are experts who volunteer their time to help improve the journal manuscripts they review-they offer authors free advice.<br><br>
Through the peer review process, manuscripts should become:</p>
       <ul style="font-size:14px;">
<li><strong>More robust: </strong>Peer reviewers may point out gaps in your paper that require more explanation or additional experiments.</li>
<li><strong>Easier to read: : </strong>If parts of your paper are difficult to understand, reviewers can tell you so that you can fix them.</li>
<li><strong>More useful: </strong>Peer reviewers also consider the importance of your paper to others in your field.</li>
</ul> 
<p align="justify">Of course, in addition to offering authors advice, another important purpose of peer review is to make sure that the manuscripts the journal eventually publishes are of high quality. If a journal publishes too many low-quality manuscripts, its reputation and number of readers will decline.</p>
<div class="panel-heading" id="headingOne" role="tab">
           <h6 class="panel-title big text-info"><a href="#">Editorial Rejection:</a></h6>
</div><hr>
<p align="justify">Your journal manuscript can be rejected if it:</p>
<ul style="font-size:14px;">
<li>Lacks proper structure</li>
<li>Lacks the necessary detail for readers to fully understand the authors' analysis</li>
<li>Has no new science</li>
<li>Does not clearly explain which parts of the findings are new science, versus what was already known</li>
<li>Lacks up-to-date references</li>
<li>Contains theories, concepts, or conclusions that are not fully supported by its data, arguments, and information</li>
<li>Does not provide enough details about materials and methods to allow other scientists to repeat the experiment</li>
<li>Lacks clear descriptions or explanations of:</li>
<li>Hypotheses tested</li>
<li>The experimental design</li>
<li>Sample characteristics and descriptive statistics</li>
<li>Describes poor experimental design, or faulty or insufficient statistical analysis</li>
<li>Has poor language quality</li>
</ul>
<p>Publication is a difficult process, and you must be prepared to defend your submission against rejection from both editors and peer reviewers. However, do not be too persistent. Generally, only one letter defending your submission will be accepted for each of the review stages (editorial review and peer review). If you are unsuccessful after sending a response letter, then you should strongly considerselecting another journal. </p>

<p align="justify">When revising your manuscript and responding to peer review comments:</p> 



<ul style="font-size:14px;">
  <li>Address all points raised by the editor and reviewers</li>
  <li>Describe the revisions to your manuscript in your response letter</li>
  <li>Perform any additional experiments or analyses the reviewers recommend (unless you feel that they would not make your paper better; if this is the case, explain why in your response letter)</li>
  <li>Provide a polite and scientific rebuttal to any points or comments you disagree with</li>
  <li>Differentiate between reviewer comments and your responses in your letter</li>
  <li>Clearly show the major revisions in the text, either with a different color text, by highlighting the changes, or with Microsoft Word's Track Changes feature</li>
  <li>Return the revised manuscript and response letter within the time period the editor tells you Examples</li>
  </ul>
  <br>
 
    <p><strong>Reviewer comment:</strong> "In your analysis of the data you have chosen to use a somewhat obscure fitting function (regression). In my opinion, a simple Gaussian function would have sufficed. Moreover, the results would be more instructive and easier to compare to previous results."<br>
      <strong>Response in agreement with the reviewer:</strong> "We agree with the reviewer's assessment of the analysis. Our tailored function does make it impossible to fully interpret the data in terms of the prevailing theories. In addition, in its current form, we agree it would be difficult to tell that this measurement constitutes a significant improvement over previously reported values. We have therefore re-analyzed the data using a Gaussian fitting function."<br>
      <strong>Response disagreeing with the reviewer:</strong>"We agree with the reviewer that a simple Gaussian fit would facilitate comparison with the results of other studies. However, our tailored function allows for the analysis of the data in terms of the Smith model [Smith et al, 1998]. We have added two sentences to the paper (page 3, paragraph 2) to explain the use of this function and Smith's model."</p>
    <p><br>
      Note that in both comments (agreeing and disagreeing) the author is polite and shows respect for the reviewer's opinion. Also, in both circumstances the author makes a change to the manuscript that addresses the reviewer's question.<br>
      Remember, the reviewer is probably a highly knowledgeable person. If their suggestion is incorrect, it is likely because they misunderstood your manuscript, indicating that you should make your text clearer.</p>
